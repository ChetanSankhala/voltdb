 package org.voltdb.sqlparser.semantics.symtab;

import org.voltdb.sqlparser.syntax.symtab.ISourceLocation;
import org.voltdb.sqlparser.syntax.symtab.ITop;

/**
 * Top is the implementation of ITop.
 *
 * @author bwhite
 *
 */
public class Top implements ITop {
    private final String m_name;
    private final ISourceLocation m_sourceLocation;

    public final ISourceLocation getSourceLocation() {
        return m_sourceLocation;
    }
    Top(ISourceLocation aLoc, String aName) {
        m_sourceLocation = aLoc;
        m_name = aName;
    }
    /* (non-Javadoc)
     * @see org.voltdb.sqlparser.symtab.ITop#getName()
     */
    @Override
    public final String getName() {
        return m_name;
    }

    @Override
    public String toString() {
        return m_name.toUpperCase();
    }

}
